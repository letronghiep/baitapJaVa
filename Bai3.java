
public class Bai3 {
	public static void main(String[] args) {
		int a=10, b=100;
		System.out.println("a) a|b = " + (a|b)); // Phep OR
		System.out.println("b) a&b = " + (a&b)); // Phep AND
		System.out.println("c) a^b = " + (a^b)); // Phep XOR
		System.out.println("d) ~a = " + (~a)); // Phep NOT
		System.out.println("e) a>>2 = " + (a>>2)); //Phep dich phai 2 bit
		System.out.println("f) a<<2 = " + (a<<2)); // Phep dich trai 2 bit
	}
}
